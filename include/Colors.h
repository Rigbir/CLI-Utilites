#define C_YELLOW "\033[33m"      // Yellow
#define C_Blue   "\033[0;34m"    // Blue
#define C_Purple "\033[0;35m"    // Purple
#define C_Cyan   "\033[0;36m"    // Cyan
#define C_White  "\033[0;37m"    // White

// Bold
#define BBlack   "\033[1;30m"    // Black

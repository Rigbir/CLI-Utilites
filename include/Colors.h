#define BGreen   "\033[1;32m"    // Green
#define BYellow  "\033[1;33m"    // Yellow
#define BBlue    "\033[1;34m"    // Blue
#define BPurple  "\033[1;35m"    // Purple
#define BCyan    "\033[1;36m"    // Cyan
